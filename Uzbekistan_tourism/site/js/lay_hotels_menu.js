function lay_hotels_appear() {
    var e = document.getElementsByClassName("lay_menu_h")[0];
        e.style.height = "500px";
        e.style.visibility = "visible";
}

function lay_hotels_disappear() {
    var e = document.getElementsByClassName("lay_menu_h")[0];
        e.style.height = "0px";
        e.style.visibility = "hidden";
}
